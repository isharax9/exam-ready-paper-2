import java.util.HashMap;

public class Hashmap {
    public static void main(String[] args) {
        // Create a HashMap
        HashMap<String, Integer> map = new HashMap<>();
        
        // Add key-value pairs to the HashMap
        map.put("Apple", 3);
        map.put("Banana", 5);
        map.put("Orange", 2);
        
        // Print the HashMap
        System.out.println(map);
    }
}

